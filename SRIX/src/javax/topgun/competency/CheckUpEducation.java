package javax.topgun.competency;

public class CheckUpEducation {

	private int idUser;
	private int idFaculty;
	private int idMajor;
	private int idEducation;
	private int idDegree;
	
	public int getIdUser() {
		return idUser;
	}
	public void setIdUser(int idUser) {
		this.idUser = idUser;
	}
	public int getIdFaculty() {
		return idFaculty;
	}
	public void setIdFaculty(int idFaculty) {
		this.idFaculty = idFaculty;
	}
	public int getIdMajor() {
		return idMajor;
	}
	public void setIdMajor(int idMajor) {
		this.idMajor = idMajor;
	}
	public int getIdEducation() {
		return idEducation;
	}
	public void setIdEducation(int idEducation) {
		this.idEducation = idEducation;
	}
	public void setIdDegree(int idDegree) {
		this.idDegree = idDegree;
	}
	public int getIdDegree() {
		return idDegree;
	}
	
}
