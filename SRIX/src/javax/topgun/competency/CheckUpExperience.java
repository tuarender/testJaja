package javax.topgun.competency;

public class CheckUpExperience {
	
	private int idUser;
	private int idJobfield;
	private int year;
	private int month;
	private int idExp;
	
	public int getIdUser() {
		return idUser;
	}
	public void setIdUser(int idUser) {
		this.idUser = idUser;
	}
	public int getIdJobfield() {
		return idJobfield;
	}
	public void setIdJobfield(int idJobfield) {
		this.idJobfield = idJobfield;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getIdExp() {
		return idExp;
	}
	public void setIdExp(int idExp) {
		this.idExp = idExp;
	}
}
