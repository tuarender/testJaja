package javax.topgun.competency;


public class CheckUpStrength {
	
	private int idUser;
	private int idStrength;
	private int idStrengthOrder;
	
	public int getIdUser() {
		return idUser;
	}
	public void setIdUser(int idUser) {
		this.idUser = idUser;
	}
	public int getIdStrength() {
		return idStrength;
	}
	public void setIdStrength(int idStrength) {
		this.idStrength = idStrength;
	}
	public void setIdStrengthOrder(int idStrengthOrder) {
		this.idStrengthOrder = idStrengthOrder;
	}
	public int getIdStrengthOrder() {
		return idStrengthOrder;
	}
	
	
}
