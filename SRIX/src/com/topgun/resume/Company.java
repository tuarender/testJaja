package com.topgun.resume;

public class Company 
{
	private int idCompany;
	private String resumeTemplate;
	private int resumeLanguage1;
	private int resumeLanguage2;
	
	public int getIdCompany() 
	{
		return idCompany;
	}
	public void setIdCompany(int idCompany) 
	{
		this.idCompany = idCompany;
	}
	public String getResumeTemplate() 
	{
		return resumeTemplate;
	}
	public void setResumeTemplate(String resumeTemp) 
	{
		this.resumeTemplate = resumeTemp;
	}
	public int getResumeLanguage1() 
	{
		return resumeLanguage1;
	}
	public void setResumeLanguage1(int resumeLanguage1) 
	{
		this.resumeLanguage1 = resumeLanguage1;
	}
	public int getResumeLanguage2() 
	{
		return resumeLanguage2;
	}
	public void setResumeLanguage2(int resumeLanguage2) 
	{
		this.resumeLanguage2 = resumeLanguage2;
	}
	
	

}
