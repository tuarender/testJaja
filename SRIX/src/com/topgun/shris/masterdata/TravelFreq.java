package com.topgun.shris.masterdata;

public class TravelFreq {
	int idLanguage;
	int idFequency;
	String frequencyName;
	int idOder;
	public int getIdLanguage() {
		return idLanguage;
	}
	public void setIdLanguage(int idLanguage) {
		this.idLanguage = idLanguage;
	}
	public int getIdFequency() {
		return idFequency;
	}
	public void setIdFequency(int idFequency) {
		this.idFequency = idFequency;
	}
	public String getFrequencyName() {
		return frequencyName;
	}
	public void setFrequencyName(String frequencyName) {
		this.frequencyName = frequencyName;
	}
	public int getIdOder() {
		return idOder;
	}
	public void setIdOder(int idOder) {
		this.idOder = idOder;
	}

}
